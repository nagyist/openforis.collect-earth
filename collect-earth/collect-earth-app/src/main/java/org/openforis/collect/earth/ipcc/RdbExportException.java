package org.openforis.collect.earth.ipcc;

public class RdbExportException extends Exception {

	public RdbExportException() {
		super();
	}

	public RdbExportException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RdbExportException(String message, Throwable cause) {
		super(message, cause);
	}

	public RdbExportException(String message) {
		super(message);
	}

	public RdbExportException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -5210016260577827042L;

}